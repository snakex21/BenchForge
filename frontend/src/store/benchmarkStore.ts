import { create } from 'zustand'
import type { Benchmark, EvaluationRubricItem, OutputType, Task } from '@/types'
import { isAutoGeneratedParentTask } from '@/utils/benchmarkTasks'
import { useResultStore } from './resultStore'
import { useUIStore } from './uiStore'

type BenchmarkInput = Omit<Benchmark, 'id' | 'created_at'>
type TaskInput = { benchmarkId: number; name: string; promptTemplate: string; scoreType: 'numeric' | 'boolean'; expectedAnswer?: 'tak' | 'nie' | null; passCondition?: string | null; evaluationChecklist?: string[]; evaluationRubric?: EvaluationRubricItem[]; attempts?: number; outputType?: OutputType; referenceImage?: string | null; orderIndex?: number }

interface BenchmarkSlice {
  benchmarks: Benchmark[]
  tasks: Task[]
  isLoading: boolean
  loadFromDb: () => Promise<void>
  addBenchmark: (benchmark: BenchmarkInput) => Promise<Benchmark | null>
  updateBenchmark: (id: number, patch: Partial<BenchmarkInput>) => Promise<void>
  removeBenchmark: (id: number) => Promise<void>
  addTask: (task: TaskInput) => Promise<void>
  updateTask: (id: number, patch: Partial<TaskInput>) => Promise<void>
  deleteTask: (id: number) => Promise<void>
}

export const useBenchmarkStore = create<BenchmarkSlice>((set) => ({
  benchmarks: [],
  tasks: [],
  isLoading: false,

  loadFromDb: async () => {
    const db = window.db
    if (!db) {
      set({ benchmarks: [], tasks: [], isLoading: false })
      return
    }

    set({ isLoading: true })
    const benchmarks = await db.getBenchmarks()
    let taskLists = db.tasks ? await Promise.all(benchmarks.map((benchmark) => db.tasks.get({ benchmarkId: benchmark.id }))) : []

    if (db.tasks) {
      const staleLegacyTasks = taskLists.flatMap((tasks, index) => {
        const benchmark = benchmarks[index]
        if (!benchmark || tasks.length <= 1) return []
        return tasks.filter((task) => isAutoGeneratedParentTask(task, benchmark))
      })
      if (staleLegacyTasks.length > 0) {
        await Promise.all(staleLegacyTasks.map((task) => db.tasks.delete({ id: task.id })))
        taskLists = await Promise.all(benchmarks.map((benchmark) => db.tasks.get({ benchmarkId: benchmark.id })))
      }
    }
    const tasks = taskLists.flat()
    set({ benchmarks: benchmarks.map((benchmark) => ({ ...benchmark, tasks: tasks.filter((task) => task.benchmark_id === benchmark.id) })), tasks, isLoading: false })
  },

  addBenchmark: async (benchmark) => {
    if (!window.db) return null
    const created = await window.db.addBenchmark(benchmark)
    set((state) => ({ benchmarks: [{ ...created, tasks: [] }, ...state.benchmarks] }))
    return created
  },

  updateBenchmark: async (id, patch) => {
    if (!window.db) return
    const updated = await window.db.updateBenchmark({ id, data: patch })
    set((state) => ({ benchmarks: state.benchmarks.map((benchmark) => benchmark.id === id ? { ...updated, tasks: benchmark.tasks || [] } : benchmark) }))
  },

  removeBenchmark: async (id) => {
    if (!window.db) return
    await window.db.deleteBenchmark({ id })
    set((state) => ({ benchmarks: state.benchmarks.filter((benchmark) => benchmark.id !== id), tasks: state.tasks.filter((task) => task.benchmark_id !== id) }))
    if (useUIStore.getState().selectedBenchmarkId === id) {
      useUIStore.getState().selectBenchmark(null)
      useUIStore.getState().setRightPanelOpen(false)
    }
    await useResultStore.getState().loadFromDb()
  },

  addTask: async (task) => {
    if (!window.db?.tasks) return
    const created = await window.db.tasks.add(task)
    set((state) => {
      const tasks = [...state.tasks, created].sort((a, b) => a.order_index - b.order_index)
      return { tasks, benchmarks: state.benchmarks.map((benchmark) => benchmark.id === task.benchmarkId ? { ...benchmark, tasks: tasks.filter((item) => item.benchmark_id === task.benchmarkId) } : benchmark) }
    })
  },

  updateTask: async (id, patch) => {
    if (!window.db?.tasks) return
    const updated = await window.db.tasks.update({ id, data: patch })
    set((state) => {
      const tasks = state.tasks.map((task) => task.id === id ? updated : task)
      return { tasks, benchmarks: state.benchmarks.map((benchmark) => benchmark.id === updated.benchmark_id ? { ...benchmark, tasks: tasks.filter((item) => item.benchmark_id === updated.benchmark_id) } : benchmark) }
    })
  },

  deleteTask: async (id) => {
    if (!window.db?.tasks) return
    const existing = useBenchmarkStore.getState().tasks.find((task) => task.id === id)
    await window.db.tasks.delete({ id })
    set((state) => {
      const tasks = state.tasks.filter((task) => task.id !== id)
      return { tasks, benchmarks: state.benchmarks.map((benchmark) => benchmark.id === existing?.benchmark_id ? { ...benchmark, tasks: tasks.filter((item) => item.benchmark_id === benchmark.id) } : benchmark) }
    })
    await useBenchmarkStore.getState().loadFromDb()
  },
}))
