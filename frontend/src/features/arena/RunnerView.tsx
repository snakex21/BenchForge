const formatScoreLabel = (score: string | null | undefined, t: (key: TranslationKey) => string) => {
  const normalized = String(score || '').toLowerCase()
  if (normalized === 'tak') return t('common.passed')
  if (normalized === 'nie') return t('common.failed')
  return score ? `${t('common.score')}: ${score}` : ''
}

const isPassingScore = (score?: string | null) => {
  const normalized = String(score ?? '').trim().toLowerCase()
  if (!normalized) return true
  if (normalized === 'tak') return true
  if (normalized === 'nie') return false
  const numeric = Number(normalized.replace(',', '.'))
  return Number.isNaN(numeric) ? normalized !== '0' : numeric >= 50
}

const formatDuration = (durationMs?: number | null) => {
  if (!durationMs || durationMs < 0) return null
  if (durationMs < 1000) return `${durationMs} ms`
  const seconds = durationMs / 1000
  if (seconds < 60) return `${seconds.toFixed(seconds >= 10 ? 1 : 2)} s`
  const minutes = Math.floor(seconds / 60)
  const rest = Math.round(seconds % 60)
  return `${minutes} min ${rest} s`
}

const formatTaskResultLabel = (taskState: RunnerTaskState, t: (key: TranslationKey, params?: Record<string, string | number | null | undefined>) => string) => {
  if (taskState.status === 'done') {
    const passed = isPassingScore(taskState.score)
    const prefix = taskState.attempt && taskState.attempt > 1 ? `${taskState.attempt}. ` : ''
    return `${prefix}${passed ? t('common.passed') : t('common.failed')}`
  }
  if (taskState.status === 'error') return t('common.failed')
  if (taskState.status === 'retrying') return taskState.attempt ? t('runner.attempt', { attempt: taskState.attempt }) : t('runner.status.retrying')
  if (taskState.status === 'running') return t('runner.status.running').replace('🔄 ', '')
  if (taskState.status === 'manual') return t('runner.status.manual')
  return '...'
}

