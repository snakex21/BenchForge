                <label className="text-sm text-slate-300">{t('common.scoreType')}<select className="mt-1 w-full rounded-lg border border-slate-700 bg-slate-950 px-3 py-2" value={form.score_type} onChange={(event) => setForm({ ...form, score_type: event.target.value as ScoreType })}><option value="numeric">numeric</option><option value="boolean">boolean</option></select></label>
