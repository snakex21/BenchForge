              {form.score_type === 'boolean' && <label className="text-sm text-slate-300">{t('common.expectedAnswer')}<select className="mt-1 w-full rounded-lg border border-slate-700 bg-slate-950 px-3 py-2" value={form.expected_answer || ''} onChange={(event) => setForm({ ...form, expected_answer: event.target.value ? event.target.value as 'tak' | 'nie' : null })}><option value="">—</option><option value="tak">tak</option><option value="nie">nie</option></select></label>}
