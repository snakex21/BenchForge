const isRecord = (value: unknown): value is Record<string, unknown> => typeof value === 'object' && value !== null && !Array.isArray(value)
const isScoreType = (value: unknown): value is ScoreType => value === 'numeric' || value === 'boolean'
const isOutputType = (value: unknown): value is OutputType => outputTypes.includes(String(value) as OutputType)
const normalizeSearch = (value: unknown) => String(value || '').toLowerCase().trim()
const promptPreview = (value?: string | null) => String(value || '').trim().replace(/\s+/g, ' ').slice(0, 280)

const checklistFromText = (value: string) => value.split('\n').map((line) => line.trim()).filter(Boolean)

const rubricFromText = (value: string) => {
  if (!value.trim()) return []
  try {
    const parsed = JSON.parse(value)
    return Array.isArray(parsed) ? parsed : []
  } catch {
    return []
  }
}

const rubricToText = (value: unknown) => Array.isArray(value) && value.length ? JSON.stringify(value, null, 2) : ''

const formatCondition = (value: string | null | undefined, t: Translate) => {
  if (!value) return null
  try {
    const parsed = JSON.parse(value)
    if (String(parsed?.mode || '').startsWith('tool_') || Array.isArray(parsed?.allowed_tools)) return `Tool benchmark: ${parsed.mode || 'tool_agent'} · ${(parsed.allowed_tools || []).length} tools`
    if (parsed?.mode === 'code_presence' && Array.isArray(parsed.required)) {
      return t('benchmarks.codeMustContain', { items: parsed.required.map(String).join(', ') })
    }
    if (Array.isArray(parsed?.required)) return t('benchmarks.requiredElements', { items: parsed.required.map(String).join(', ') })
    if (parsed?.expected !== undefined) return String(parsed.expected)
    return JSON.stringify(parsed, null, 2)
  } catch {
    return value
  }
}

const benchmarkMatchesSearch = (benchmark: Benchmark, query: string) => {
  if (!query) return true
  const fields = [
    benchmark.name,
    benchmark.category,
    benchmark.suite_name,
    benchmark.description,
    benchmark.prompt_template,
    benchmark.pass_condition,
    benchmark.score_type,
    ...(benchmark.evaluation_checklist || []),
    ...(benchmark.tasks || []).flatMap((task) => [task.name, task.prompt_template, task.pass_condition, task.expected_answer, ...(task.evaluation_checklist || [])]),
  ]
  return fields.some((field) => normalizeSearch(field).includes(query))
}

const parseCodePresenceRequired = (value?: string | null): string[] | null => {
  if (!value?.trim()) return null
  try {
    const parsed = JSON.parse(value)
    if (parsed?.mode === 'code_presence' && Array.isArray(parsed.required)) return parsed.required.map(String)
    return null
  } catch {
    return null
  }
}

const DEFAULT_ALLOWED_TOOLS = ['python.run', 'node.run', 'file.write', 'file.read', 'file.list', 'image.draw_path_svg', 'mcp.list_tools', 'mcp.call']

const parseToolCondition = (value?: string | null) => {
  if (!value?.trim()) return null
  try {
    const parsed = JSON.parse(value)
    const mode = String(parsed?.mode || '')
    if (!mode.startsWith('tool_') && !Array.isArray(parsed?.allowed_tools)) return null
    return {
      mode: mode || 'tool_agent',
      allowed_tools: Array.isArray(parsed.allowed_tools) ? parsed.allowed_tools.map(String) : DEFAULT_ALLOWED_TOOLS,
      denied_tools: Array.isArray(parsed.denied_tools) ? parsed.denied_tools.map(String) : [],
      mcp_allowed_tools: Array.isArray(parsed.mcp_allowed_tools) ? parsed.mcp_allowed_tools.map(String) : [],
      mcp_denied_tools: Array.isArray(parsed.mcp_denied_tools) ? parsed.mcp_denied_tools.map(String) : [],
      max_calls: Math.max(1, Number(parsed.max_calls || 8)),
      timeout_ms: Math.max(1000, Number(parsed.timeout_ms || 10000)),
      required_artifacts: Array.isArray(parsed.required_artifacts) ? parsed.required_artifacts.map(String) : [],
      raw: parsed as Record<string, unknown>,
    }
  } catch {
    return null
  }
}

const toToolCondition = (config: { mode: string; allowed_tools: string[]; denied_tools?: string[]; mcp_allowed_tools?: string[]; mcp_denied_tools?: string[]; max_calls: number; timeout_ms: number; required_artifacts: string[]; raw?: Record<string, unknown> }) => JSON.stringify({
  ...(config.raw || {}),
  mode: config.mode,
  allowed_tools: config.allowed_tools,
  denied_tools: config.denied_tools || [],
  mcp_allowed_tools: config.mcp_allowed_tools || [],
  mcp_denied_tools: config.mcp_denied_tools || [],
  max_calls: config.max_calls,
  timeout_ms: config.timeout_ms,
  required_artifacts: config.required_artifacts,
}, null, 2)

const makeDefaultToolCondition = (mode = 'tool_agent') => toToolCondition({
  mode,
  allowed_tools: DEFAULT_ALLOWED_TOOLS,
  denied_tools: [],
  mcp_allowed_tools: [],
  mcp_denied_tools: [],
  max_calls: 8,
  timeout_ms: 10000,
  required_artifacts: mode === 'tool_maze_path' ? ['path.json', 'path-overlay.svg'] : [],
  raw: mode === 'tool_maze_path' ? { start: [0, 0], end: [4, 4], grid: ['S....', '###..', '...#.', '.#...', '...#E'] } : {},
})

const toCodePresenceCondition = (requiredText: string) => JSON.stringify({
  mode: 'code_presence',
  required: requiredText.split('\n').map((line) => line.trim()).filter(Boolean),
})

