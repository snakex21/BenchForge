import React, { useMemo, useState } from 'react'
import { useBenchmarkStore } from '@/store/benchmarkStore'
import { useModelStore } from '@/store/modelStore'
import { useResultStore } from '@/store/resultStore'
import { useUIStore } from '@/store/uiStore'
import { Button } from '@/components/ui/Button'
import { Card } from '@/components/ui/Card'
import { Badge } from '@/components/ui/Badge'
import { EmptyState } from '@/components/ui/EmptyState'
import { ModelLogo } from '@/components/ui/ModelLogo'
import { MarkdownViewer, SaveResponseButton } from '@/components/benchmark/OutputViewer'
import { useTranslation } from '@/i18n'
import { isAutoGeneratedParentTask } from '@/utils/benchmarkTasks'
import { getEffectiveScoreNorm } from '@/utils/scoring'

type ResultsSection = 'models' | 'benchmarks'

const escapeHtml = (value: unknown) => String(value ?? '')
  .replace(/&/g, '&amp;')
  .replace(/</g, '&lt;')
  .replace(/>/g, '&gt;')
  .replace(/"/g, '&quot;')
  .replace(/'/g, '&#039;')

const mdCell = (value: unknown) => String(value ?? '—').replace(/\|/g, '\\|').replace(/\r?\n/g, '<br>')
const truncate = (value: unknown, max = 320) => {
  const text = String(value || '').replace(/\s+/g, ' ').trim()
  return text.length > max ? `${text.slice(0, max)}…` : text
}

export const ResultsView: React.FC = () => {
  const results = useResultStore((state) => state.results)
  const runs = useResultStore((state) => state.runs)
  const removeResult = useResultStore((state) => state.removeResult)
  const clearResults = useResultStore((state) => state.clearResults)
  const models = useModelStore((state) => state.models)
  const benchmarks = useBenchmarkStore((state) => state.benchmarks)
  const setActiveView = useUIStore((state) => state.setActiveView)
  const setRerunTarget = useUIStore((state) => state.setRerunTarget)
  const { locale, t } = useTranslation()
  const [section, setSection] = useState<ResultsSection>('models')
  const [expandedModels, setExpandedModels] = useState<number[]>([])
  const [expandedBenchmarks, setExpandedBenchmarks] = useState<number[]>([])
  const [selectedResultId, setSelectedResultId] = useState<number | null>(null)
  const [selectedModalTaskId, setSelectedModalTaskId] = useState<number | null>(null)
  const [judgeReport, setJudgeReport] = useState<Record<string, unknown> | null>(null)

  const modelById = useMemo(() => new Map(models.map((model) => [model.id, model])), [models])
  const benchmarkById = useMemo(() => new Map(benchmarks.map((benchmark) => [benchmark.id, benchmark])), [benchmarks])
  const mainResults = useMemo(() => {
    const aggregates = results.filter((result) => result.task_id == null)
    return aggregates.length > 0 ? aggregates : results
  }, [results])

  const byModel = useMemo(() => {
    const groups = new Map<number, typeof results>()
    for (const result of mainResults) groups.set(result.model_id, [...(groups.get(result.model_id) || []), result])
    return Array.from(groups.entries()).map(([modelId, items]) => ({ modelId, modelName: modelById.get(modelId)?.name || `Model #${modelId}`, results: [...items].sort((a, b) => new Date(b.run_at).getTime() - new Date(a.run_at).getTime()) }))
  }, [mainResults, modelById])

  const byBenchmark = useMemo(() => {
    const groups = new Map<number, typeof results>()
    for (const result of mainResults) groups.set(result.benchmark_id, [...(groups.get(result.benchmark_id) || []), result])
    return Array.from(groups.entries()).map(([benchmarkId, items]) => ({ benchmarkId, benchmarkName: benchmarkById.get(benchmarkId)?.name || `Benchmark #${benchmarkId}`, results: [...items].sort((a, b) => new Date(b.run_at).getTime() - new Date(a.run_at).getTime()) }))
  }, [benchmarkById, mainResults])

  const formatDate = (value?: string | null) => {
    if (!value) return '—'
    const m = String(value).match(/^(\d{4})-(\d{2})-(\d{2}) (\d{2}):(\d{2})(?::(\d{2}))?$/)
    if (!m) return '—'
    const [, year, month, day, hour, minute] = m
    const date = new Date(Date.UTC(Number(year), Number(month) - 1, Number(day), Number(hour), Number(minute)))
    return date.toLocaleString(locale, { day: '2-digit', month: '2-digit', year: 'numeric', hour: '2-digit', minute: '2-digit' })
  }
  const formatDuration = (value?: number | null) => value ? `${(value / 1000).toFixed(1)}s` : '—'
  const formatTokens = (value?: number | null) => value === null || value === undefined ? '—' : String(value)
  const formatCost = (value?: number | null) => value === null || value === undefined || Number(value) <= 0 ? '—' : `$${Number(value).toFixed(Number(value) < 0.01 ? 4 : 2)}`
  const isPassingScore = (score?: string | null) => {
    const normalized = String(score ?? '').trim().toLowerCase()
    if (!normalized) return false
    if (normalized === 'tak') return true
    if (normalized === 'nie') return false
    const numeric = Number(normalized.replace(',', '.'))
    return Number.isNaN(numeric) ? normalized !== '0' : numeric >= 50
  }
  const toggleModel = (modelId: number) => setExpandedModels((current) => current.includes(modelId) ? current.filter((id) => id !== modelId) : [...current, modelId])
  const toggleBenchmark = (benchmarkId: number) => setExpandedBenchmarks((current) => current.includes(benchmarkId) ? current.filter((id) => id !== benchmarkId) : [...current, benchmarkId])
  const selectedResult = selectedResultId ? results.find((result) => result.id === selectedResultId) || null : null
  const selectedBenchmark = selectedResult ? benchmarkById.get(selectedResult.benchmark_id) || null : null
  const selectedModel = selectedResult ? modelById.get(selectedResult.model_id) || null : null
  const selectedTasks = selectedBenchmark?.tasks || []
  const selectedVisibleTasks = selectedBenchmark?.tasks?.filter((task) => !((selectedBenchmark.tasks?.length || 0) > 1 && isAutoGeneratedParentTask(task, selectedBenchmark))) || []
  const selectedTaskFromResult = selectedTasks.find((task) => task.id === selectedResult?.task_id) || null
  const selectedTask = (selectedModalTaskId ? selectedVisibleTasks.find((task) => task.id === selectedModalTaskId) : selectedTaskFromResult) || (selectedResult?.task_id == null ? selectedVisibleTasks[0] : null) || null
  const selectedTaskIndex = selectedTask ? selectedVisibleTasks.findIndex((task) => task.id === selectedTask.id) : -1

  const extractAggregateTaskResponse = (notes?: string | null, taskIndex = -1) => {
    if (!notes || taskIndex < 0) return notes || ''
    const sections = notes.split(/\n\n---\n\n/g)
    const section = sections[taskIndex]
    if (!section) return notes
    return section.replace(/^##\s+\d+\.\s+[^\n]*\n/i, '').replace(/^Wynik:\s*[^\n]*\n+/i, '').trim()
  }

  const extractAggregateTaskThinking = extractAggregateTaskResponse

  const getRunTaskResults = (result: typeof results[number]) => {
    if (result.task_id != null) return [result]
    const matching = results.filter((item) => item.model_id === result.model_id && item.benchmark_id === result.benchmark_id)
    const previousAggregate = matching
      .filter((item) => item.task_id == null && item.id < result.id)
      .sort((a, b) => b.id - a.id)[0]

    return matching
      .filter((item) => item.task_id != null && item.id < result.id && (!previousAggregate || item.id > previousAggregate.id))
      .sort((a, b) => a.id - b.id)
  }
  const modalResults = selectedResult ? getRunTaskResults(selectedResult) : []
  const selectedTaskResult = selectedTask ? modalResults.find((result) => result.task_id === selectedTask.id) || null : null
  const selectedDisplayResult = selectedTaskResult || selectedResult
  const selectedResponse = selectedTaskResult?.notes || (selectedResult && selectedResult.task_id == null && selectedTask
    ? extractAggregateTaskResponse(selectedResult.notes, selectedTaskIndex)
    : selectedResult?.notes || '')
  const selectedThinking = selectedTaskResult?.thinking_notes || (selectedResult && selectedResult.task_id == null && selectedTask
    ? extractAggregateTaskThinking(selectedResult.thinking_notes, selectedTaskIndex)
    : selectedResult?.thinking_notes || '')
  const selectedAgentReport = useMemo(() => {
    if (!selectedResponse?.trim()) return null
    try {
      const parsed = JSON.parse(selectedResponse)
      if (!parsed || typeof parsed !== 'object') return null
      if (!('verification' in parsed) && !('workdir' in parsed) && !('final_answer' in parsed)) return null
      return parsed as { final_answer?: unknown; verification?: { passed?: boolean; reason?: string; pathPoints?: unknown[] }; workdir?: string; path?: unknown[] }
    } catch {
      return null
    }
  }, [selectedResponse])
  const selectedSandboxReport = useMemo(() => {
    if (!selectedThinking?.trim()) return null
    const match = selectedThinking.match(/##\s+Sandbox evaluation[\s\S]*?```json\s*([\s\S]*?)```/i)
    if (!match?.[1]) return null
    try {
      const parsed = JSON.parse(match[1])
      if (!parsed || typeof parsed !== 'object') return null
      return parsed as { ok?: boolean; score?: number; passed?: number; total?: number; tests?: Array<{ index?: number; test?: string; passed?: boolean; error?: string }>; workdir?: string; repoDir?: string; language?: string; type?: string }
    } catch {
      return null
    }
  }, [selectedThinking])
  const getRunSummary = (result: typeof results[number]) => {
    const benchmark = benchmarkById.get(result.benchmark_id)
    const taskResults = getRunTaskResults(result)
    const visibleTasks = benchmark?.tasks?.filter((task) => !((benchmark.tasks?.length || 0) > 1 && isAutoGeneratedParentTask(task, benchmark))) || []
    const total = visibleTasks.length || taskResults.length || 1
    const completed = taskResults.length || (result.task_id == null ? total : 1)
    const taskScoreNorm = (item: typeof results[number]) => {
      const task = visibleTasks.find((entry) => entry.id === item.task_id) || null
      return benchmark ? getEffectiveScoreNorm(item, benchmark, task) : (isPassingScore(item.score) ? 1 : 0)
    }
    const resultNorm = benchmark ? getEffectiveScoreNorm(result, benchmark, null) : (isPassingScore(result.score) ? 1 : 0)
    const passed = taskResults.length ? taskResults.filter((item) => taskScoreNorm(item) > 0).length : (resultNorm > 0 ? completed : 0)
    const avgNorm = taskResults.length ? taskResults.reduce((sum, item) => sum + taskScoreNorm(item), 0) / total : resultNorm
    const duration = taskResults.length ? taskResults.reduce((sum, item) => sum + (Number(item.duration_ms) || 0), 0) : Number(result.duration_ms) || 0
    const tokens = taskResults.length ? taskResults.reduce((sum, item) => sum + (Number(item.tokens_used) || 0), 0) : Number(result.tokens_used) || 0
    const cost = taskResults.length ? taskResults.reduce((sum, item) => sum + (Number(item.estimated_cost_usd) || 0), 0) : Number(result.estimated_cost_usd) || 0
    const thinkingCount = taskResults.length ? taskResults.filter((item) => item.thinking_notes?.trim()).length : (result.thinking_notes?.trim() ? 1 : 0)
    return { total, completed, passed, duration, tokens, cost, thinkingCount, avgPercent: Math.round(avgNorm * 100) }
  }
  const selectedSummary = selectedResult ? getRunSummary(selectedResult) : null

  const renderScore = (score: string) => {
    const normalized = String(score).toLowerCase()
    if (normalized === 'tak' || normalized === 'nie') return <Badge variant={normalized === 'tak' ? 'success' : 'danger'}>{normalized === 'tak' ? t('common.passed') : t('common.failed')}</Badge>
    return <Badge variant="info">{t('common.score')}: {score}</Badge>
  }

  const handleRerun = (modelId: number, benchmarkId: number) => {
    setRerunTarget({ modelId, benchmarkId })
    setActiveView('runner')
  }

  const getFailedTaskIds = (result: typeof results[number]) => {
    const benchmark = benchmarkById.get(result.benchmark_id)
    if (!benchmark) return []
    const visibleTasks = (benchmark.tasks || []).filter((task) => !((benchmark.tasks?.length || 0) > 1 && isAutoGeneratedParentTask(task, benchmark)))
    return getRunTaskResults(result)
      .filter((item) => item.task_id != null)
      .filter((item) => {
        const task = visibleTasks.find((entry) => entry.id === item.task_id) || null
        return getEffectiveScoreNorm(item, benchmark, task) <= 0
      })
      .map((item) => Number(item.task_id))
  }

  const handleRerunFailed = (result: typeof results[number]) => {
    const taskIds = getFailedTaskIds(result)
    if (taskIds.length === 0) return
    setRerunTarget({ modelId: result.model_id, benchmarkId: result.benchmark_id, taskIds })
    setActiveView('runner')
  }

  const handleDeleteResult = async (id: number) => {
    if (!confirm(t('results.deleteConfirm'))) return
    await removeResult(id)
    if (selectedResultId === id) setSelectedResultId(null)
  }

  const handleOpenArtifacts = async (artifactPath?: string | null) => {
    if (!artifactPath) {
      alert(t('results.artifactsUnavailable'))
      return
    }
    await window.benchforge?.openPath?.(artifactPath)
  }

  const handleExportArtifactsZip = async (artifactPath?: string | null) => {
    if (!artifactPath) {
      alert(t('results.artifactsUnavailable'))
      return
    }
    await window.benchforge?.exportArtifactZip?.({ relativePath: artifactPath, defaultFileName: `benchforge-artifacts-${Date.now()}.zip` })
  }

  const handleJudge = async () => {
    if (!selectedDisplayResult || !selectedBenchmark) return
    const judgeModelId = await window.db?.getPreference?.('judge_model_id')
    if (typeof judgeModelId !== 'number') {
      alert(t('settings.judgeModelDescription'))
      return
    }
    const result = await window.benchforge?.judgeEvaluate?.({ modelId: judgeModelId, taskPrompt: selectedTask?.prompt_template || selectedBenchmark.prompt_template, checklist: selectedTask?.evaluation_checklist || selectedBenchmark.evaluation_checklist || [], response: selectedResponse })
    setJudgeReport(result || null)
  }

  const handleExportCSV = () => {
    const headers = ['model_id', 'model_name', 'benchmark_id', 'benchmark_name', 'score', 'tokens_used', 'input_tokens', 'output_tokens', 'estimated_cost_usd', 'duration_ms', 'run_at', 'notes']
    const header = headers.join(',')
    const rows = results.map((r) => [
      r.model_id,
      `"${(modelById.get(r.model_id)?.name || '').replace(/"/g, '""')}"`,
      r.benchmark_id,
      `"${(benchmarkById.get(r.benchmark_id)?.name || '').replace(/"/g, '""')}"`,
      `"${String(r.score ?? '')}"`,
      r.tokens_used ?? '',
      r.input_tokens ?? '',
      r.output_tokens ?? '',
      r.estimated_cost_usd ?? '',
      r.duration_ms ?? '',
      `"${String(r.run_at || '')}"`,
      `"${String(r.notes || '').replace(/"/g, '""').slice(0, 500)}"`,
    ].join(','))
    const csv = [header, ...rows].join('\n')
    const blob = new Blob(['\uFEFF' + csv], { type: 'text/csv;charset=utf-8' })
    const url = URL.createObjectURL(blob)
    const anchor = document.createElement('a')
    anchor.href = url
    anchor.download = `benchforge-results-${Date.now()}.csv`
    anchor.click()
    URL.revokeObjectURL(url)
  }

  const handleExport = async () => {
    const data = JSON.stringify({ results, runs }, null, 2)
    if (window.benchforge?.saveJsonFile) {
      await window.benchforge.saveJsonFile({ defaultFileName: `benchforge-results-${Date.now()}.json`, content: data })
      return
    }
    const blob = new Blob([data], { type: 'application/json' })
    const url = URL.createObjectURL(blob)
    const anchor = document.createElement('a')
    anchor.href = url
    anchor.download = `benchforge-results-${Date.now()}.json`
    anchor.click()
    URL.revokeObjectURL(url)
  }

  const getReportTaskRows = (runResult: typeof results[number]) => {
    const benchmark = benchmarkById.get(runResult.benchmark_id)
    const visibleTasks = benchmark?.tasks?.filter((task) => !((benchmark.tasks?.length || 0) > 1 && isAutoGeneratedParentTask(task, benchmark))) || []
    const taskResults = getRunTaskResults(runResult)
    if (taskResults.length === 0) return []
    return taskResults.map((taskResult, index) => {
      const task = visibleTasks.find((entry) => entry.id === taskResult.task_id) || null
      const scoreNorm = benchmark ? getEffectiveScoreNorm(taskResult, benchmark, task) : (isPassingScore(taskResult.score) ? 1 : 0)
      return {
        id: taskResult.id,
        modelName: modelById.get(taskResult.model_id)?.name || `Model #${taskResult.model_id}`,
        benchmarkName: benchmark?.name || `Benchmark #${taskResult.benchmark_id}`,
        taskName: task?.name || `${t('common.task')} ${index + 1}`,
        score: taskResult.score,
        percent: Math.round(scoreNorm * 100),
        passed: scoreNorm >= 0.5,
        duration: taskResult.duration_ms || null,
        tokens: taskResult.tokens_used || null,
        runAt: taskResult.run_at,
        artifactPath: taskResult.artifact_path || runResult.artifact_path || null,
        responsePreview: taskResult.response_preview || truncate(taskResult.notes, 240),
      }
    })
  }

  const buildReportData = () => {
    const runRows = mainResults
      .map((result) => {
        const summary = getRunSummary(result)
        const modelName = modelById.get(result.model_id)?.name || `Model #${result.model_id}`
        const benchmarkName = benchmarkById.get(result.benchmark_id)?.name || `Benchmark #${result.benchmark_id}`
        return { result, summary, modelName, benchmarkName }
      })
      .sort((a, b) => new Date(b.result.run_at).getTime() - new Date(a.result.run_at).getTime())

    const ranking = Array.from(runRows.reduce((map, row) => {
      const current = map.get(row.result.model_id) || { modelId: row.result.model_id, modelName: row.modelName, runs: 0, scoreSum: 0, passed: 0, total: 0, tokens: 0, duration: 0 }
      current.runs += 1
      current.scoreSum += row.summary.avgPercent
      current.passed += row.summary.passed
      current.total += row.summary.total
      current.tokens += row.summary.tokens
      current.duration += row.summary.duration
      map.set(row.result.model_id, current)
      return map
    }, new Map<number, { modelId: number; modelName: string; runs: number; scoreSum: number; passed: number; total: number; tokens: number; duration: number }>()).values())
      .map((row) => ({ ...row, average: Math.round(row.scoreSum / Math.max(1, row.runs)) }))
      .sort((a, b) => b.average - a.average)

    const benchmarkRows = runRows.map((row) => ({
      modelName: row.modelName,
      benchmarkName: row.benchmarkName,
      score: row.summary.avgPercent,
      passed: row.summary.passed,
      total: row.summary.total,
      tokens: row.summary.tokens,
      duration: row.summary.duration,
      runAt: row.result.run_at,
      artifactPath: row.result.artifact_path || null,
    }))

    const taskRows = runRows.flatMap((row) => getReportTaskRows(row.result))
    const sortedTasks = [...taskRows].sort((a, b) => b.percent - a.percent)
    return {
      generatedAt: new Date(),
      runRows,
      ranking,
      benchmarkRows,
      taskRows,
      bestTasks: sortedTasks.slice(0, 10),
      worstTasks: [...sortedTasks].reverse().slice(0, 10),
    }
  }

  const buildMarkdownReport = () => {
    const report = buildReportData()
    const totalTasks = report.taskRows.length
    const passedTasks = report.taskRows.filter((task) => task.passed).length
    const average = report.ranking.length ? Math.round(report.ranking.reduce((sum, row) => sum + row.average, 0) / report.ranking.length) : 0
    const lines = [
      '# BenchForge Report',
      '',
      `Generated: ${report.generatedAt.toLocaleString(locale)}`,
      '',
      '## Summary',
      '',
      `- Models: ${models.length}`,
      `- Benchmarks: ${benchmarks.length}`,
      `- Runs/results: ${mainResults.length}`,
      `- Task details: ${totalTasks}`,
      `- Passed tasks: ${passedTasks}/${totalTasks}`,
      `- Average ranking score: ${average}%`,
      '',
      '## Model ranking',
      '',
      '| # | Model | Average | Runs | Passed tasks | Tokens | Duration |',
      '|---:|---|---:|---:|---:|---:|---:|',
      ...report.ranking.map((row, index) => `| ${index + 1} | ${mdCell(row.modelName)} | ${row.average}% | ${row.runs} | ${row.passed}/${row.total} | ${row.tokens || '—'} | ${formatDuration(row.duration)} |`),
      '',
      '## Benchmark results',
      '',
      '| Benchmark | Model | Score | Pass | Tokens | Duration | Date | Artifacts |',
      '|---|---|---:|---:|---:|---:|---|---|',
      ...report.benchmarkRows.map((row) => `| ${mdCell(row.benchmarkName)} | ${mdCell(row.modelName)} | ${row.score}% | ${row.passed}/${row.total} | ${row.tokens || '—'} | ${formatDuration(row.duration)} | ${mdCell(formatDate(row.runAt))} | ${mdCell(row.artifactPath || '—')} |`),
      '',
      '## Best tasks',
      '',
      '| Task | Benchmark | Model | Score | Result |',
      '|---|---|---|---:|---|',
      ...report.bestTasks.map((task) => `| ${mdCell(task.taskName)} | ${mdCell(task.benchmarkName)} | ${mdCell(task.modelName)} | ${task.percent}% | ${task.passed ? 'PASS' : 'FAIL'} |`),
      '',
      '## Worst tasks',
      '',
      '| Task | Benchmark | Model | Score | Result |',
      '|---|---|---|---:|---|',
      ...report.worstTasks.map((task) => `| ${mdCell(task.taskName)} | ${mdCell(task.benchmarkName)} | ${mdCell(task.modelName)} | ${task.percent}% | ${task.passed ? 'PASS' : 'FAIL'} |`),
      '',
      '## Task-level details',
      '',
      '| Task | Benchmark | Model | Score | Result | Tokens | Duration | Artifacts | Preview |',
      '|---|---|---|---:|---|---:|---:|---|---|',
      ...report.taskRows.map((task) => `| ${mdCell(task.taskName)} | ${mdCell(task.benchmarkName)} | ${mdCell(task.modelName)} | ${task.percent}% | ${task.passed ? 'PASS' : 'FAIL'} | ${task.tokens || '—'} | ${formatDuration(task.duration)} | ${mdCell(task.artifactPath || '—')} | ${mdCell(task.responsePreview || '—')} |`),
      '',
    ]
    return lines.join('\n')
  }

  const buildHtmlReport = () => {
    const report = buildReportData()
    const totalTasks = report.taskRows.length
    const passedTasks = report.taskRows.filter((task) => task.passed).length
    const average = report.ranking.length ? Math.round(report.ranking.reduce((sum, row) => sum + row.average, 0) / report.ranking.length) : 0
    const badge = (ok: boolean) => `<span class="badge ${ok ? 'ok' : 'fail'}">${ok ? 'PASS' : 'FAIL'}</span>`
    const rows = (items: string[]) => items.join('\n')
    return `<!doctype html>
<html lang="en"><head><meta charset="utf-8"><title>BenchForge Report</title><style>
body{font-family:Inter,Segoe UI,Arial,sans-serif;background:#0f1117;color:#e2e8f0;margin:0;padding:32px}h1,h2{margin:0 0 12px}.card{border:1px solid #334155;background:#161822;border-radius:16px;padding:18px;margin:16px 0}.grid{display:grid;grid-template-columns:repeat(auto-fit,minmax(180px,1fr));gap:12px}.metric{border:1px solid #334155;border-radius:12px;padding:12px;background:#0f1117}.muted{color:#94a3b8}table{width:100%;border-collapse:collapse;margin-top:10px}th,td{border-bottom:1px solid #334155;padding:8px;text-align:left;font-size:13px;vertical-align:top}th{color:#cbd5e1}.bar{height:10px;background:#334155;border-radius:999px;overflow:hidden;min-width:120px}.bar>span{display:block;height:100%;background:#6366f1}.badge{display:inline-block;border-radius:8px;padding:2px 7px;font-size:11px;font-weight:700}.badge.ok{background:#064e3b;color:#a7f3d0}.badge.fail{background:#7f1d1d;color:#fecaca}pre{white-space:pre-wrap;max-height:160px;overflow:auto;background:#020617;padding:10px;border-radius:8px}</style></head><body>
<h1>BenchForge Report</h1><p class="muted">Generated: ${escapeHtml(report.generatedAt.toLocaleString(locale))}</p>
<section class="card"><h2>Summary</h2><div class="grid"><div class="metric"><strong>${models.length}</strong><br><span class="muted">Models</span></div><div class="metric"><strong>${benchmarks.length}</strong><br><span class="muted">Benchmarks</span></div><div class="metric"><strong>${mainResults.length}</strong><br><span class="muted">Runs/results</span></div><div class="metric"><strong>${passedTasks}/${totalTasks}</strong><br><span class="muted">Passed tasks</span></div><div class="metric"><strong>${average}%</strong><br><span class="muted">Average ranking score</span></div></div></section>
<section class="card"><h2>Model ranking</h2><table><thead><tr><th>#</th><th>Model</th><th>Average</th><th>Chart</th><th>Runs</th><th>Passed</th><th>Tokens</th><th>Duration</th></tr></thead><tbody>${rows(report.ranking.map((row, index) => `<tr><td>${index + 1}</td><td>${escapeHtml(row.modelName)}</td><td>${row.average}%</td><td><div class="bar"><span style="width:${row.average}%"></span></div></td><td>${row.runs}</td><td>${row.passed}/${row.total}</td><td>${row.tokens || '—'}</td><td>${escapeHtml(formatDuration(row.duration))}</td></tr>`))}</tbody></table></section>
<section class="card"><h2>Benchmark results</h2><table><thead><tr><th>Benchmark</th><th>Model</th><th>Score</th><th>Pass</th><th>Tokens</th><th>Duration</th><th>Date</th><th>Artifacts</th></tr></thead><tbody>${rows(report.benchmarkRows.map((row) => `<tr><td>${escapeHtml(row.benchmarkName)}</td><td>${escapeHtml(row.modelName)}</td><td>${row.score}%</td><td>${row.passed}/${row.total}</td><td>${row.tokens || '—'}</td><td>${escapeHtml(formatDuration(row.duration))}</td><td>${escapeHtml(formatDate(row.runAt))}</td><td>${escapeHtml(row.artifactPath || '—')}</td></tr>`))}</tbody></table></section>
<section class="card"><h2>Task-level details</h2><table><thead><tr><th>Task</th><th>Benchmark</th><th>Model</th><th>Score</th><th>Result</th><th>Tokens</th><th>Duration</th><th>Artifacts</th><th>Preview</th></tr></thead><tbody>${rows(report.taskRows.map((task) => `<tr><td>${escapeHtml(task.taskName)}</td><td>${escapeHtml(task.benchmarkName)}</td><td>${escapeHtml(task.modelName)}</td><td>${task.percent}%</td><td>${badge(task.passed)}</td><td>${task.tokens || '—'}</td><td>${escapeHtml(formatDuration(task.duration))}</td><td>${escapeHtml(task.artifactPath || '—')}</td><td>${escapeHtml(task.responsePreview || '—')}</td></tr>`))}</tbody></table></section>
</body></html>`
  }

  const handleExportReport = async (format: 'markdown' | 'html') => {
    const isHtml = format === 'html'
    const content = isHtml ? buildHtmlReport() : buildMarkdownReport()
    await window.benchforge?.saveTextFile?.({
      defaultFileName: `benchforge-report-${Date.now()}.${isHtml ? 'html' : 'md'}`,
      extension: isHtml ? 'html' : 'md',
      extensionLabel: isHtml ? 'HTML' : 'Markdown',
      content,
    })
  }

  const ResultCard = ({ result, label }: { result: typeof results[number]; label: string }) => (
    <div role="button" tabIndex={0} className="w-full cursor-pointer rounded-lg border border-slate-700/30 bg-[#11131b] p-[clamp(0.75rem,1vw,1rem)] text-left transition hover:border-indigo-400/60 hover:bg-indigo-500/5" onClick={() => { setSelectedResultId(result.id); setSelectedModalTaskId(benchmarkById.get(result.benchmark_id)?.tasks?.[0]?.id || null) }} onKeyDown={(event) => { if (event.key === 'Enter' || event.key === ' ') { setSelectedResultId(result.id); setSelectedModalTaskId(benchmarkById.get(result.benchmark_id)?.tasks?.[0]?.id || null) } }}>
      <div className="flex flex-wrap gap-2 text-xs text-slate-300">
        <Badge variant="neutral">{label}</Badge>
        <Badge variant="neutral">{formatDate(result.run_at)}</Badge>
        <button className="rounded px-2 py-0.5 text-cyan-300 hover:bg-cyan-500/10" type="button" onClick={(event) => { event.stopPropagation(); handleRerun(result.model_id, result.benchmark_id) }}>{t('results.rerun')}</button>
        {getFailedTaskIds(result).length > 0 && <button className="rounded px-2 py-0.5 text-orange-300 hover:bg-orange-500/10" type="button" onClick={(event) => { event.stopPropagation(); handleRerunFailed(result) }}>{t('results.rerunFailed')}</button>}
        <button className="rounded px-2 py-0.5 text-emerald-300 hover:bg-emerald-500/10" type="button" onClick={(event) => { event.stopPropagation(); void handleOpenArtifacts(result.artifact_path) }}>{t('results.openArtifacts')}</button>
        <button className="rounded px-2 py-0.5 text-purple-300 hover:bg-purple-500/10" type="button" onClick={(event) => { event.stopPropagation(); void handleExportArtifactsZip(result.artifact_path) }}>{t('results.exportArtifactsZip')}</button>
        <button className="ml-auto rounded px-2 py-0.5 text-red-300 hover:bg-red-500/10" type="button" onClick={(event) => { event.stopPropagation(); void handleDeleteResult(result.id) }}>{t('common.delete')}</button>
      </div>
      <div className="mt-2 flex flex-wrap items-center gap-3 text-sm text-slate-400">
        {(() => { const summary = getRunSummary(result); return <><Badge variant="info">{t('common.score')}: {summary.avgPercent}%</Badge><Badge variant={summary.passed === summary.total ? 'success' : 'danger'}>{t('common.passCount', { passed: summary.passed, total: summary.total })}</Badge><span>{t('common.time')}: {formatDuration(summary.duration || result.duration_ms)}</span><span>{t('common.tokens')}: {summary.tokens || formatTokens(result.tokens_used)}</span><span>{t('common.cost')}: {formatCost(summary.cost || result.estimated_cost_usd)}</span>{summary.thinkingCount > 0 && <span>{t('common.modelThinking')}: {summary.thinkingCount}/{summary.total}</span>}</> })()}
      </div>
      <details className="mt-3 rounded-lg border border-slate-700/30 bg-slate-950/60 p-2" onClick={(event) => event.stopPropagation()}>
        <summary className="cursor-pointer text-xs font-medium text-slate-300">{t('results.viewResponse')}</summary>
        <div className="mt-2 max-h-[35vh] overflow-auto rounded bg-slate-950/80 p-3"><MarkdownViewer content={result.notes || t('results.noSavedAnswer')} className="text-xs text-slate-300" /></div>
      </details>
    </div>
  )

  return (
    <div className="space-y-6">
      <div className="flex flex-wrap items-center justify-between gap-3">
        <div>
          <h2 className="text-xl font-bold text-slate-100">{t('results.title')}</h2>
          <p className="text-sm text-slate-500">{t('results.subtitle')}</p>
        </div>
        <div className="flex flex-wrap gap-2">
          {results.length > 0 && <><Button variant="secondary" size="sm" onClick={() => void handleExportReport('markdown')}>{t('results.exportReportMarkdown')}</Button><Button variant="secondary" size="sm" onClick={() => void handleExportReport('html')}>{t('results.exportReportHtml')}</Button><Button variant="secondary" size="sm" onClick={() => void handleExport()}>{t('results.exportJson')}</Button><Button variant="secondary" size="sm" onClick={handleExportCSV}>{t('results.exportCsv')}</Button><Button variant="danger" size="sm" onClick={() => { if (confirm(t('results.clearConfirm'))) void clearResults() }}>{t('results.clear')}</Button></>}
        </div>
      </div>

      {runs.length === 0 && results.length === 0 ? <EmptyState icon="chart" title={t('results.emptyTitle')} description={t('results.emptyDescription')} actionLabel={t('results.goToArena')} onAction={() => setActiveView('arena')} /> : <>
        <div className="flex flex-wrap gap-2"><Button variant={section === 'models' ? 'primary' : 'secondary'} size="sm" onClick={() => setSection('models')}>{t('common.models')}</Button><Button variant={section === 'benchmarks' ? 'primary' : 'secondary'} size="sm" onClick={() => setSection('benchmarks')}>{t('common.benchmarks')}</Button></div>

        {section === 'models' && <Card title={t('results.modelsSection')} subtitle={`${mainResults.length} ${t('common.records')}`}><div className="space-y-3">{byModel.map((group) => { const expanded = expandedModels.includes(group.modelId); const model = modelById.get(group.modelId); return <section key={group.modelId} className="rounded-xl border border-slate-700/40 bg-slate-950/20"><button className="flex w-full items-center justify-between gap-3 px-[clamp(0.75rem,1.2vw,1rem)] py-3 text-left" onClick={() => toggleModel(group.modelId)}><span className="flex min-w-0 items-center gap-3"><span className="text-sm text-slate-400">{expanded ? '▼' : '▶'}</span><ModelLogo model={model} size="sm" /><span className="min-w-0"><span className="block truncate text-sm font-semibold text-slate-200">{group.modelName}</span>{model && <span className="mt-1 flex flex-wrap gap-1.5"><span className="rounded-md border border-indigo-500/30 bg-indigo-500/10 px-1.5 py-0.5 text-[10px] font-semibold uppercase tracking-wide text-indigo-200">{model.mode}</span>{model.provider && <span className="rounded-md border border-slate-600/50 bg-slate-800/60 px-1.5 py-0.5 text-[10px] text-slate-300">{model.provider}</span>}</span>}</span></span><Badge variant="neutral">{group.results.length} {t('common.results').toLowerCase()}</Badge></button>{expanded && <div className="grid grid-cols-[repeat(auto-fit,minmax(min(28rem,100%),1fr))] gap-3 border-t border-slate-700/30 p-3">{group.results.map((result) => <ResultCard key={result.id} result={result} label={`${t('common.benchmark')}: ${benchmarkById.get(result.benchmark_id)?.name || `#${result.benchmark_id}`}`} />)}</div>}</section> })}</div></Card>}

        {section === 'benchmarks' && <Card title={t('results.benchmarksSection')} subtitle={t('results.benchmarksSubtitle')}><div className="space-y-3">{byBenchmark.map((group) => { const expanded = expandedBenchmarks.includes(group.benchmarkId); return <section key={group.benchmarkId} className="rounded-xl border border-slate-700/40 bg-slate-950/20"><button className="flex w-full items-center justify-between gap-3 px-[clamp(0.75rem,1.2vw,1rem)] py-3 text-left" onClick={() => toggleBenchmark(group.benchmarkId)}><span className="truncate text-sm font-semibold text-slate-200">{expanded ? '▼' : '▶'} {group.benchmarkName}</span><Badge variant="neutral">{group.results.length} {t('common.responses')}</Badge></button>{expanded && <div className="grid grid-cols-[repeat(auto-fit,minmax(min(28rem,100%),1fr))] gap-3 border-t border-slate-700/30 p-3">{group.results.map((result) => { const model = modelById.get(result.model_id); return <ResultCard key={result.id} result={result} label={`${t('common.model')}: ${model?.name || `#${result.model_id}`}`} /> })}</div>}</section> })}</div></Card>}

        {runs.length > 0 && <Card title={t('results.runHistory')} subtitle={`${runs.length} ${t('common.sessions')}`}><div className="grid grid-cols-[repeat(auto-fit,minmax(min(22rem,100%),1fr))] gap-3">{runs.map((run) => <div key={run.id} className="rounded-lg border border-slate-700/30 p-3"><div className="flex items-center justify-between gap-2"><span className="truncate text-sm font-medium text-slate-300">{run.name || t('common.noName')}</span><Badge variant={run.status === 'completed' ? 'success' : 'neutral'}>{run.status}</Badge></div><p className="mt-1 text-xs text-slate-500">{t('common.start')}: {formatDate(run.started_at)}</p>{run.finished_at && <p className="text-xs text-slate-500">{t('common.end')}: {formatDate(run.finished_at)}</p>}</div>)}</div></Card>}
      </>}

      {selectedResult && selectedBenchmark && <div className="fixed inset-0 z-50 flex items-center justify-center bg-black/70 p-2 sm:p-4" onClick={() => setSelectedResultId(null)}>
        <div className="max-h-[92vh] w-full max-w-6xl overflow-auto rounded-2xl border border-slate-700/60 bg-[#1b1e2b] shadow-2xl" onClick={(event) => event.stopPropagation()}>
          <div className="flex flex-wrap items-start justify-between gap-3 border-b border-slate-700/50 p-3 sm:p-5">
            <div>
              <p className="text-xs font-semibold uppercase tracking-wide text-indigo-300">{t('results.previewTitle')}</p>
              <div className="mt-1 flex items-center gap-3">
                <ModelLogo model={selectedModel} size="sm" />
                <div>
                  <h3 className="text-xl font-semibold text-slate-100">{selectedBenchmark.name}</h3>
                  <p className="mt-1 text-sm text-slate-500">{selectedModel?.name || `Model #${selectedResult.model_id}`} · {formatDate(selectedResult.run_at)}</p>
                </div>
              </div>
            </div>
            <div className="flex flex-wrap items-center gap-2">
              {selectedDisplayResult?.task_id != null
                ? renderScore(selectedDisplayResult.score)
                : selectedSummary
                  ? <Badge variant="info">{t('common.score')}: {selectedSummary.avgPercent}%</Badge>
                  : renderScore(selectedResult.score)}
              <Button variant="secondary" size="sm" onClick={() => handleRerun(selectedResult.model_id, selectedResult.benchmark_id)}>{t('results.rerun')}</Button>
              {getFailedTaskIds(selectedResult).length > 0 && <Button variant="secondary" size="sm" onClick={() => handleRerunFailed(selectedResult)}>{t('results.rerunFailed')}</Button>}
              <Button variant="secondary" size="sm" onClick={() => void handleOpenArtifacts(selectedResult.artifact_path)}>{t('results.openArtifacts')}</Button>
              <Button variant="secondary" size="sm" onClick={() => void handleExportArtifactsZip(selectedResult.artifact_path)}>{t('results.exportArtifactsZip')}</Button>
              <Button variant="secondary" size="sm" onClick={() => void handleJudge()}>{t('results.judge')}</Button>
              <Button variant="danger" size="sm" onClick={() => void handleDeleteResult(selectedResult.id)}>{t('results.deleteResult')}</Button>
              <Button variant="ghost" size="sm" onClick={() => setSelectedResultId(null)}>{t('common.close')}</Button>
            </div>
          </div>

          <div className="grid gap-4 p-3 sm:p-5 xl:grid-cols-[320px_minmax(0,1fr)]">
            <aside className="space-y-3">
              <div className="rounded-xl border border-indigo-400/50 bg-indigo-500/10 p-3">
                <div className="flex items-center gap-3">
                  <ModelLogo model={selectedModel} size="sm" />
                  <div className="min-w-0">
                    <p className="truncate text-sm font-semibold text-slate-100">{selectedBenchmark.name}</p>
                    <p className="truncate text-xs text-slate-500">{selectedModel?.name || `Model #${selectedResult.model_id}`}</p>
                  </div>
                </div>
              </div>
              <div className="grid gap-2">
                {(selectedVisibleTasks.length ? selectedVisibleTasks.map((task, index) => {
                  const taskResult = modalResults.find((result) => result.task_id === task.id)
                  return { id: task.id, number: index + 1, name: task.name, result: taskResult || selectedResult, taskId: task.id }
                }) : modalResults.map((result, index) => ({ id: result.id, number: index + 1, name: `Wynik ${index + 1}`, result, taskId: null }))).map((item) => (
                  <button key={item.id} type="button" disabled={!item.result} className={`flex items-center justify-between gap-2 rounded-lg border px-3 py-2 text-left text-sm transition ${item.taskId === selectedTask?.id || (!selectedTask && item.result?.id === selectedResult.id && !item.taskId) ? 'border-indigo-400/70 bg-indigo-500/15 text-slate-100' : 'border-slate-700/40 bg-slate-950/30 text-slate-400 hover:bg-slate-800/50 disabled:cursor-not-allowed disabled:opacity-50'}`} onClick={() => { if (!item.result) return; if (item.taskId) setSelectedModalTaskId(item.taskId); else setSelectedResultId(item.result.id) }}>
                    <span className="min-w-0 truncate">{item.number}. {item.name}</span>
                    <span className="shrink-0 text-xs">{item.result ? ((item.taskId && selectedBenchmark ? getEffectiveScoreNorm(item.result, selectedBenchmark, selectedVisibleTasks.find((task) => task.id === item.taskId) || null) > 0 : isPassingScore(item.result.score)) ? t('common.passed') : t('common.failed')) : '—'}</span>
                  </button>
                ))}
              </div>
            </aside>

            <section className="min-w-0 space-y-4 rounded-2xl border border-slate-700/50 bg-slate-950/30 p-4 shadow-inner shadow-black/10">
              <div className="flex flex-wrap items-start justify-between gap-3 border-b border-slate-800 pb-4">
                <div className="min-w-0">
                  <p className="text-xs font-semibold uppercase tracking-wide text-indigo-300">{t('results.currentResult')}</p>
                  <h4 className="mt-1 text-lg font-semibold text-slate-100">{selectedTask?.name || selectedBenchmark.name}</h4>
                  <p className="mt-1 text-sm text-slate-500">{selectedBenchmark.name} · {formatDate(selectedResult.run_at)}</p>
                </div>
                <div className="flex flex-wrap gap-2">
                  <Badge>{selectedBenchmark.category}</Badge>
                  <Badge variant="info">{selectedTask?.score_type || selectedBenchmark.score_type}</Badge>
                  {selectedDisplayResult && renderScore(selectedDisplayResult.score)}
                  {selectedSummary && <Badge variant={selectedSummary.passed === selectedSummary.total ? 'success' : 'danger'}>{t('common.passCount', { passed: selectedSummary.passed, total: selectedSummary.total })}</Badge>}
                  <Badge variant="neutral">{t('common.time')}: {formatDuration(selectedDisplayResult?.duration_ms)}</Badge>
                  <Badge variant="neutral">{t('common.tokens')}: {formatTokens(selectedDisplayResult?.tokens_used)}</Badge>
                  <Badge variant="neutral">{t('common.cost')}: {formatCost(selectedDisplayResult?.estimated_cost_usd)}</Badge>
                  {selectedThinking?.trim() && <Badge variant="neutral">{t('common.thinkingSaved')}</Badge>}
                </div>
              </div>

              <div className="rounded-xl border border-slate-700/40 bg-[#101322] p-3">
                <p className="mb-2 text-xs font-semibold uppercase tracking-wide text-slate-500">{t('common.taskPrompt')}</p>
                <pre className="max-h-56 overflow-auto whitespace-pre-wrap text-sm leading-relaxed text-slate-200">{selectedTask?.prompt_template || selectedBenchmark.prompt_template || t('common.noPrompt')}</pre>
              </div>

              {((selectedTask?.expected_answer || selectedTask?.pass_condition || selectedTask?.evaluation_checklist?.length) || (selectedBenchmark.expected_answer || selectedBenchmark.pass_condition || selectedBenchmark.evaluation_checklist?.length)) && <div className="rounded-xl border border-emerald-500/20 bg-emerald-500/10 p-3 text-sm text-emerald-100">
                {(selectedTask?.expected_answer || selectedBenchmark.expected_answer) && <p>{t('common.expected')}: <strong>{(selectedTask?.expected_answer || selectedBenchmark.expected_answer)?.toUpperCase()}</strong></p>}
                {(selectedTask?.pass_condition || selectedBenchmark.pass_condition) && <p className="whitespace-pre-wrap">{t('common.condition')}: {selectedTask?.pass_condition || selectedBenchmark.pass_condition}</p>}
                {(selectedTask?.evaluation_checklist || selectedBenchmark.evaluation_checklist || []).length > 0 && <ul className="mt-2 list-disc space-y-1 pl-4">{(selectedTask?.evaluation_checklist || selectedBenchmark.evaluation_checklist || []).map((entry, index) => <li key={`${entry}-${index}`}>{entry}</li>)}</ul>}
              </div>}

              {selectedAgentReport && <div className="rounded-xl border border-cyan-500/30 bg-cyan-500/10 p-3 text-sm text-cyan-50">
                <div className="mb-3 flex flex-wrap items-center justify-between gap-2">
                  <p className="text-xs font-semibold uppercase tracking-wide text-cyan-200">{t('results.agentReport')}</p>
                  {selectedAgentReport.verification && <Badge variant={selectedAgentReport.verification.passed ? 'success' : 'danger'}>{selectedAgentReport.verification.passed ? t('common.passed') : t('common.failed')}</Badge>}
                </div>
                {selectedAgentReport.final_answer !== undefined && <p className="whitespace-pre-wrap"><strong>{t('results.agentFinalAnswer')}:</strong> {String(selectedAgentReport.final_answer)}</p>}
                {selectedAgentReport.verification && <p className="mt-2"><strong>{t('results.agentVerification')}:</strong> {selectedAgentReport.verification.reason || '—'}</p>}
                {(selectedAgentReport.path || selectedAgentReport.verification?.pathPoints) && <p className="mt-2"><strong>{t('results.agentPathPoints')}:</strong> {(selectedAgentReport.path || selectedAgentReport.verification?.pathPoints || []).length}</p>}
                {selectedAgentReport.workdir && <div className="mt-2 flex flex-wrap items-center gap-2"><span className="text-xs text-cyan-100/80">{selectedAgentReport.workdir}</span><Button variant="ghost" size="sm" onClick={() => void window.benchforge?.openPath?.(selectedAgentReport.workdir || '')}>{t('runner.openToolArtifacts')}</Button></div>}
              </div>}

              {selectedSandboxReport && <div className="rounded-xl border border-violet-500/30 bg-violet-500/10 p-3 text-sm text-violet-50">
                <div className="mb-3 flex flex-wrap items-center justify-between gap-2">
                  <p className="text-xs font-semibold uppercase tracking-wide text-violet-200">{t('results.sandboxReport')}</p>
                  <Badge variant={selectedSandboxReport.ok ? 'success' : 'danger'}>{selectedSandboxReport.passed ?? 0}/{selectedSandboxReport.total ?? 0} · {selectedSandboxReport.score ?? 0}%</Badge>
                </div>
                <p className="text-xs text-violet-100/80">{selectedSandboxReport.type || selectedSandboxReport.language || 'sandbox'}</p>
                {(selectedSandboxReport.workdir || selectedSandboxReport.repoDir) && <div className="mt-2 flex flex-wrap items-center gap-2"><span className="text-xs text-violet-100/80">{t('results.sandboxWorkdir')}: {selectedSandboxReport.repoDir || selectedSandboxReport.workdir}</span><Button variant="ghost" size="sm" onClick={() => void window.benchforge?.openPath?.(selectedSandboxReport.repoDir || selectedSandboxReport.workdir || '')}>{t('runner.openToolArtifacts')}</Button></div>}
                {Array.isArray(selectedSandboxReport.tests) && selectedSandboxReport.tests.length > 0 && <details className="mt-3 rounded-lg border border-slate-700/40 bg-slate-950/40 p-2">
                  <summary className="cursor-pointer text-xs text-slate-300">{t('results.sandboxTests')}</summary>
                  <div className="mt-2 space-y-2">
                    {selectedSandboxReport.tests.map((test, index) => <div key={index} className="rounded bg-black/30 p-2 text-xs"><Badge variant={test.passed ? 'success' : 'danger'}>{test.passed ? t('common.passed') : t('common.failed')}</Badge><pre className="mt-2 whitespace-pre-wrap text-slate-300">{test.test}</pre>{test.error && <pre className="mt-2 max-h-32 overflow-auto whitespace-pre-wrap text-red-200">{test.error}</pre>}</div>)}
                  </div>
                </details>}
              </div>}

              {judgeReport && <div className="rounded-xl border border-amber-500/30 bg-amber-500/10 p-3 text-sm text-amber-50">
                <p className="mb-2 text-xs font-semibold uppercase tracking-wide text-amber-200">{t('results.judgeReport')}</p>
                <pre className="max-h-72 overflow-auto rounded bg-black/30 p-3 text-xs text-slate-200">{JSON.stringify(judgeReport, null, 2)}</pre>
              </div>}

              {selectedThinking?.trim() && <details className="rounded-xl border border-slate-700/40 bg-slate-950/50 p-3">
                <summary className="cursor-pointer text-xs font-semibold uppercase tracking-wide text-slate-400">{t('common.modelThinking')}</summary>
                <div className="mt-3 max-h-72 overflow-auto rounded-lg bg-slate-950/80 p-3"><MarkdownViewer content={selectedThinking} className="text-xs text-slate-400" /></div>
              </details>}

              <div className="rounded-xl border border-slate-700/40 bg-slate-950 p-3">
                <div className="mb-2 flex items-center justify-between gap-3">
                  <p className="text-xs font-semibold uppercase tracking-wide text-slate-500">{t('common.modelAnswer')}</p>
                  <span className="text-xs text-slate-500">{t('common.time')}: {formatDuration(selectedDisplayResult?.duration_ms)} · {t('common.tokens')}: {formatTokens(selectedDisplayResult?.tokens_used)}</span>
                </div>
                <div className="mb-2 flex justify-end">
                  <SaveResponseButton content={selectedResponse} outputType={selectedBenchmark?.output_type || 'text'} fileName={`${selectedModel?.name || 'model'}_${selectedBenchmark?.name || 'benchmark'}.txt`} />
                </div>
                <div className="min-h-72 rounded-lg bg-[#070b17] p-4"><MarkdownViewer content={selectedResponse || t('results.noSavedAnswer')} /></div>
              </div>
            </section>
          </div>
        </div>
      </div>}

      <Button variant="ghost" size="sm" onClick={() => setActiveView('arena')}>{t('results.backToArena')}</Button>
    </div>
  )
}
