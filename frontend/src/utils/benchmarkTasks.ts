export function extractNumberedTasks(promptTemplate?: string | null): string[] {
  const text = promptTemplate || ''
  const lines = text.split(/\r?\n/)
  const tasks: string[] = []

  for (const line of lines) {
    const trimmed = line.trim()
    const match = trimmed.match(/^(\d{1,3})[.)]\s+(.+)$/)
    if (!match) continue
    tasks.push(`${match[1]}. ${match[2].trim()}`)
  }

  return tasks
}

export interface BenchmarkTaskSpec {
  number: number
  title: string
  expected?: string | null
}

type ParentBenchmarkLike = {
  prompt_template?: string | null
  pass_condition?: string | null
}

type TaskLike = {
  name?: string | null
  prompt_template?: string | null
  pass_condition?: string | null
  expected_answer?: string | null
  evaluation_checklist?: string[] | null
}

export function isAutoGeneratedParentTask(task: TaskLike, benchmark: ParentBenchmarkLike): boolean {
  const taskPrompt = String(task.prompt_template || '').trim()
  const benchmarkPrompt = String(benchmark.prompt_template || '').trim()
  if (!taskPrompt || !benchmarkPrompt || taskPrompt !== benchmarkPrompt) return false

  const genericName = /^Zadanie\s+1$/i.test(String(task.name || '').trim())
  const sameCondition = String(task.pass_condition || '').trim() === String(benchmark.pass_condition || '').trim()
  const noOwnChecks = !task.expected_answer && !task.pass_condition && (!task.evaluation_checklist || task.evaluation_checklist.length === 0)

  return genericName && (sameCondition || noOwnChecks)
}

export function extractTaskSpecs(promptTemplate?: string | null, passCondition?: string | null): BenchmarkTaskSpec[] {
  if (passCondition) {
    try {
      const parsed = JSON.parse(passCondition)
      if (Array.isArray(parsed?.tasks)) {
        const rawTasks = parsed.tasks as unknown[]
        return rawTasks
          .filter((task: unknown): task is Record<string, unknown> => typeof task === 'object' && task !== null)
          .map((task: Record<string, unknown>, index: number) => ({
            number: typeof task.number === 'number' ? task.number : index + 1,
            title: String(task.title || task.question || `Zadanie ${index + 1}`),
            expected: task.expected === undefined || task.expected === null ? null : String(task.expected),
          }))
      }
    } catch {
      // Non-JSON pass_condition is allowed; fall back to parsing prompt text.
    }
  }

  return extractNumberedTasks(promptTemplate).map((task, index) => ({
    number: index + 1,
    title: task.replace(/^\d{1,3}[.)]\s*/, ''),
    expected: null,
  }))
}
