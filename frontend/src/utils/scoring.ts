import type { Benchmark, BenchmarkResult, Task } from '@/types'
import { isAutoGeneratedParentTask } from './benchmarkTasks'

function escapeRegex(value: string) {
  return String(value).replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}

function hasExactDeclaration(text: string, identifier: string) {
  const id = escapeRegex(identifier)
  const boundary = '[^a-z0-9_$]'
  const declarationPatterns = [
    new RegExp(`(^|${boundary})(?:def|function|func|fun|fn|class|struct|interface|trait|protocol)\\s+${id}(?=$|${boundary})`, 'i'),
    new RegExp(`(^|${boundary})(?:const|let|var)\\s+${id}\\s*=`, 'i'),
    new RegExp(`(^|${boundary})(?:public\\s+|private\\s+|protected\\s+|static\\s+|final\\s+|inline\\s+|constexpr\\s+|async\\s+)*(?:[a-z_$][\\w$:<>,\\[\\]\\*&?]*\\s+)+${id}\\s*\\(`, 'i'),
  ]
  return declarationPatterns.some((pattern) => pattern.test(text))
}

function hasPrefixedBadDeclaration(text: string, identifier: string) {
  const id = escapeRegex(identifier)
  const boundary = '[^a-z0-9_$]'
  const badIdentifier = `${id}[a-z0-9_$]+`
  const badPatterns = [
    new RegExp(`(^|${boundary})(?:def|function|func|fun|fn|class|struct|interface|trait|protocol)\\s+${badIdentifier}(?=$|${boundary})`, 'i'),
    new RegExp(`(^|${boundary})(?:const|let|var)\\s+${badIdentifier}\\s*=`, 'i'),
    new RegExp(`(^|${boundary})(?:public\\s+|private\\s+|protected\\s+|static\\s+|final\\s+|inline\\s+|constexpr\\s+|async\\s+)*(?:[a-z_$][\\w$:<>,\\[\\]\\*&?]*\\s+)+${badIdentifier}\\s*\\(`, 'i'),
  ]
  return badPatterns.some((pattern) => pattern.test(text))
}

function matchesRequiredCodeFragment(text: string, required: string) {
  const needle = String(required || '').trim().toLowerCase()
  if (!needle) return false

  if (needle === 'seen' && (/\bwidziane\b/i.test(text) || /\bseen\b/i.test(text) || /\bset\s*\(/i.test(text) || /dict\.fromkeys/i.test(text))) {
    return true
  }

  if (/^[a-z_$][\w$]*$/i.test(needle)) {
    const methodAccessPattern = new RegExp(`\\.\\s*${escapeRegex(needle)}(?=$|[^a-z0-9_$])`, 'i')
    if (methodAccessPattern.test(text)) return true

    if (hasPrefixedBadDeclaration(text, needle) && !hasExactDeclaration(text, needle)) return false
    const identifierPattern = new RegExp(`(^|[^a-z0-9_$])${escapeRegex(needle)}(?=$|[^a-z0-9_$])`, 'i')
    return identifierPattern.test(text)
  }

  const declarationMatch = needle.match(/^((?:def|function|func|fun|fn|class|struct|interface|trait|protocol)\s+)([a-z_$][\w$]*)$/i)
  if (declarationMatch) {
    const [, prefix, identifier] = declarationMatch
    const declarationPattern = new RegExp(`(^|[^a-z0-9_$])${escapeRegex(prefix)}${escapeRegex(identifier)}(?=$|[^a-z0-9_$])`, 'i')
    return declarationPattern.test(text)
  }

  return text.includes(needle)
}

function evaluateCodePresenceNorm(passCondition?: string | null, response?: string | null) {
  try {
    const parsed = JSON.parse(passCondition || '{}')
    if (parsed?.mode !== 'code_presence' || !Array.isArray(parsed.required)) return null
    const required = parsed.required.map((needle: unknown) => String(needle || '').trim()).filter(Boolean)
    if (required.length === 0) return null
    const text = String(response || '').toLowerCase()
    if (!text.trim()) return 0
    return required.every((needle: string) => matchesRequiredCodeFragment(text, needle)) ? 1 : 0
  } catch {
    return null
  }
}

export function normalizeScore(
  score: string,
  scoreType: 'numeric' | 'boolean',
  expectedAnswer?: string | null,
  attemptNumber?: number,
  attempts?: number
): number {
  const value = String(score).trim().toLowerCase()

  if (scoreType === 'boolean') {
    const expected = expectedAnswer === 'tak' || expectedAnswer === 'nie' ? expectedAnswer : 'tak'
    const base = value === expected ? 1 : 0
    if (!base) return 0
    return attemptNumber && attempts && attempts > 0 ? (attempts - attemptNumber + 1) / attempts : 1
  }

  // Numeric — treat as pure percentage (0–100), clamp to 0–1
  const numeric = Number(value)
  if (Number.isNaN(numeric) || numeric < 0) return 0
  return Math.min(numeric / 100, 1)
}

export function getEffectiveScoreNorm(result: BenchmarkResult, benchmark: Benchmark, task?: Task | null): number {
  const codePresenceNorm = evaluateCodePresenceNorm(task?.pass_condition ?? benchmark.pass_condition, result.notes)
  if (codePresenceNorm !== null) return codePresenceNorm
  return normalizeScore(result.score, task?.score_type || benchmark.score_type, task?.expected_answer ?? benchmark.expected_answer, result.attempt_number, task?.attempts || benchmark.attempts)
}

export function getModelBenchmarkScore(
  modelId: number,
  benchmarkId: number,
  results: BenchmarkResult[],
  benchmark: Benchmark
): { scoreNorm: number, scoreRaw: string, percent: number } | null {
  const matching = results.filter((result) => result.model_id === modelId && result.benchmark_id === benchmarkId)
  const benchmarkTasks = (benchmark.tasks || []).filter((task) => !((benchmark.tasks?.length || 0) > 1 && isAutoGeneratedParentTask(task, benchmark)))
  const taskIds = new Set(benchmarkTasks.map((task) => task.id).filter((id): id is number => typeof id === 'number'))

  // For multi-task benchmark: always compute from latest task results, never from aggregate
  if (taskIds.size > 0) {
    const aggregates = matching
      .filter((result) => result.task_id == null)
      .sort((a, b) => b.id - a.id)
    const latestAggregate = aggregates[0]
    const previousAggregate = aggregates[1]

    // Get the latest result for each task within the latest run window
    const taskResults: Array<{ taskId: number; result: BenchmarkResult | undefined; passed: boolean }> = Array.from(taskIds).map((taskId) => {
      const candidates = matching.filter((result) => {
        if (result.task_id !== taskId) return false
        if (latestAggregate && result.id > latestAggregate.id) return false
        if (previousAggregate && result.id <= previousAggregate.id) return false
        return true
      })
      const best = candidates.sort((a, b) => b.id - a.id)[0]
      const task = benchmarkTasks.find((t) => t.id === taskId)
      const passed = best ? getEffectiveScoreNorm(best, benchmark, task) > 0 : false
      return { taskId, result: best, passed }
    })

    const completed = taskResults.filter((t) => t.result).length
    const passed = taskResults.filter((t) => t.passed).length

    if (completed > 0) {
      const scoreNorm = taskResults.reduce((sum, tr) => {
        if (!tr.result) return sum
        const task = benchmarkTasks.find((t) => t.id === tr.taskId)
        return sum + getEffectiveScoreNorm(tr.result, benchmark, task)
      }, 0) / taskIds.size
      return { scoreNorm, scoreRaw: `${passed}/${taskIds.size}`, percent: Math.round(scoreNorm * 100) }
    }

    // No task results at all — show 0
    return { scoreNorm: 0, scoreRaw: `0/${taskIds.size}`, percent: 0 }
  }

  // Single-result benchmark (no explicit tasks): use the latest/best result
  const aggregate = matching
    .filter((result) => result.task_id == null)
    .sort((a, b) => new Date(b.run_at).getTime() - new Date(a.run_at).getTime())[0]

  const best = aggregate || matching
    .sort((a, b) => normalizeScore(b.score, benchmark.score_type, benchmark.expected_answer, b.attempt_number, benchmark.attempts) - normalizeScore(a.score, benchmark.score_type, benchmark.expected_answer, a.attempt_number, benchmark.attempts))[0]

  if (!best) return null
  const scoreNorm = getEffectiveScoreNorm(best, benchmark, null)
  return { scoreNorm, scoreRaw: `${Math.round(scoreNorm * 100)}%`, percent: Math.round(scoreNorm * 100) }
}

export function getModelSummary(
  modelId: number,
  results: BenchmarkResult[],
  benchmarks: Benchmark[]
): { avgNorm: number, avgDisplay: string, percent: number } {
  const scored = benchmarks
    .map((benchmark) => getModelBenchmarkScore(modelId, benchmark.id, results, benchmark))
    .filter((item): item is { scoreNorm: number, scoreRaw: string, percent: number } => Boolean(item))

  if (!scored.length) return { avgNorm: 0, avgDisplay: '—', percent: 0 }
  const avgNorm = scored.reduce((sum, item) => sum + item.scoreNorm, 0) / scored.length
  const percent = Math.round(avgNorm * 100)
  return { avgNorm, avgDisplay: `${percent}%`, percent }
}
