// ============================================================
// DetailsPanel — prawy panel szczegółów wybranego elementu
// ============================================================

import React from 'react'
import { useUIStore } from '@/store/uiStore'
import { useBenchmarkStore } from '@/store/benchmarkStore'
import { useModelStore } from '@/store/modelStore'
import { useResultStore } from '@/store/resultStore'
import { Card } from '../ui/Card'
import { Badge } from '../ui/Badge'
import { ScoreBadge } from '../ui/ScoreBadge'
import { ModelLogo } from '../ui/ModelLogo'
import { extractTaskSpecs, isAutoGeneratedParentTask } from '@/utils/benchmarkTasks'
import { getModelBenchmarkScore } from '@/utils/scoring'
import { useTranslation } from '@/i18n'

export const DetailsPanel: React.FC = () => {
  const rightPanelOpen = useUIStore((s) => s.rightPanelOpen)
  const selectedModelId = useUIStore((s) => s.selectedModelId)
  const selectedBenchmarkId = useUIStore((s) => s.selectedBenchmarkId)
  const { t } = useTranslation()

  if (!rightPanelOpen) return null

  // Determine what's selected
  const model = selectedModelId
    ? useModelStore.getState().models.find((m) => m.id === selectedModelId)
    : null
  const benchmark = selectedBenchmarkId
    ? useBenchmarkStore.getState().benchmarks.find(
        (b) => b.id === selectedBenchmarkId
      )
    : null
  const benchmarks = useBenchmarkStore.getState().benchmarks
  const models = useModelStore.getState().models
  const results = useResultStore.getState().results

  const renderScoreSummary = (summary: { scoreRaw: string; percent: number } | null) => {
    if (!summary) return <span className="text-xs text-slate-600">—</span>
    if (/^\d+(?:\.\d+)?$/.test(String(summary.scoreRaw))) {
      return <ScoreBadge score={summary.percent} size="sm" />
    }
    const label = summary.scoreRaw.includes('/') ? `${summary.percent}%` : summary.scoreRaw === 'tak' ? t('common.passed') : t('common.failed')
    return <Badge variant={summary.percent > 0 ? 'success' : 'danger'}>{label}</Badge>
  }

  const modelResults = selectedModelId
    ? benchmarks
      .map((bm) => ({ benchmark: bm, score: getModelBenchmarkScore(selectedModelId, bm.id, results, bm) }))
      .filter((item) => Boolean(item.score))
    : []

  const benchmarkResults = selectedBenchmarkId
    ? models
      .map((mdl) => ({ model: mdl, score: benchmark ? getModelBenchmarkScore(mdl.id, selectedBenchmarkId, results, benchmark) : null }))
      .filter((item) => Boolean(item.score))
    : []
  const benchmarkTasks = benchmark?.tasks?.length
    ? benchmark.tasks
      .filter((task) => !((benchmark.tasks?.length || 0) > 1 && isAutoGeneratedParentTask(task, benchmark)))
      .map((task, index) => ({
        number: index + 1,
        title: task.name,
        expected: task.expected_answer || task.pass_condition || (task.evaluation_checklist?.length ? task.evaluation_checklist.join('; ') : null),
      }))
    : benchmark ? extractTaskSpecs(benchmark.prompt_template, benchmark.pass_condition) : []

  return (
    <aside className="fixed inset-y-0 right-0 z-40 w-[min(22rem,calc(100vw-0.75rem))] max-w-none border-l border-slate-700/40 bg-[#161822] overflow-auto shadow-2xl shadow-black/40 xl:static xl:z-auto xl:w-[clamp(15rem,22vw,21rem)] xl:max-w-[34vw] xl:shadow-none shrink-0">
      <div className="p-4 space-y-4">
        {/* Model details */}
        {model && (
          <Card title={model.name}>
            <div className="space-y-3 text-sm">
              <div className="flex items-center gap-3">
                <ModelLogo model={model} size="lg" />
                <div className="min-w-0">
                  <p className="truncate text-slate-200 font-medium">{model.name}</p>
                  <p className="truncate text-xs text-slate-500">{model.provider || model.mode}</p>
                </div>
              </div>
              <div>
                <span className="text-slate-500 text-xs uppercase tracking-wider">{t('common.mode')}</span>
                <p className="text-slate-200 font-medium mt-0.5">{model.mode}</p>
              </div>
              <div>
                <span className="text-slate-500 text-xs uppercase tracking-wider">{t('common.provider')}</span>
                <p className="text-slate-200 font-medium mt-0.5">{model.provider || '—'}</p>
              </div>
              {model.model_id && (
                <div>
                <span className="text-slate-500 text-xs uppercase tracking-wider">Model ID</span>
                <p className="text-slate-300 mt-0.5 leading-relaxed">{model.model_id}</p>
              </div>
              )}
              {model.base_url && (
                <div>
                  <span className="text-slate-500 text-xs uppercase tracking-wider">Base URL</span>
                  <p className="text-slate-300 mt-0.5 leading-relaxed break-all">{model.base_url}</p>
                </div>
              )}
            </div>
          </Card>
        )}

        {/* Benchmark details */}
        {benchmark && (
          <Card title={benchmark.name}>
            <div className="space-y-3 text-sm">
              <Badge variant="info">{benchmark.category}</Badge>
              <span className="text-[10px] px-1.5 py-0.5 rounded bg-slate-700/60 text-slate-400">
                {benchmark.score_type}
              </span>
              {benchmark.description && (
                <p className="text-slate-300 leading-relaxed">{benchmark.description}</p>
              )}
              {benchmark.prompt_template ? (
                <div>
                  <span className="text-slate-500 text-xs uppercase tracking-wider">{t('common.tasks')}</span>
                  {benchmarkTasks.length > 0 ? (
                    <ol className="mt-1 space-y-1 rounded-lg border border-slate-700/30 bg-[#0f1117] p-2 text-xs text-slate-400">
                      {benchmarkTasks.map((task) => <li key={`${task.number}-${task.title}`}><span>{task.number}. {task.title}</span>{task.expected && <p className="mt-1 text-emerald-300/80">{t('common.expected')}: {task.expected}</p>}</li>)}
                    </ol>
                  ) : (
                    <pre className="mt-1 p-2 rounded-lg bg-[#0f1117] text-xs text-slate-400 whitespace-pre-wrap font-mono leading-relaxed max-h-32 overflow-auto border border-slate-700/30">
                      {benchmark.prompt_template}
                    </pre>
                  )}
                </div>
              ) : (
                <p className="text-xs text-amber-400">⚠️ {t('common.noPrompt')}</p>
              )}
            </div>
          </Card>
        )}

        {/* Model results */}
        {model && modelResults.length > 0 && (
          <Card title={t('details.modelResults')} subtitle={`${modelResults.length} ${t('common.benchmarks').toLowerCase()}`}>
            <div className="space-y-2">
              {modelResults.map(({ benchmark: bm, score }) => {
                return (
                  <div
                    key={bm.id}
                    className="flex items-center justify-between py-1.5 border-b border-slate-700/20 last:border-0"
                  >
                    <span className="text-xs text-slate-400 truncate flex-1">
                      {bm?.name}
                    </span>
                    {renderScoreSummary(score)}
                  </div>
                )
              })}
            </div>
          </Card>
        )}

        {/* Benchmark results */}
        {benchmark && !model && benchmarkResults.length > 0 && (
          <Card title={t('details.benchmarkResults')} subtitle={`${benchmarkResults.length} ${t('common.models').toLowerCase()}`}>
            <div className="space-y-2">
              {benchmarkResults.map(({ model: mdl, score }) => {
                return (
                  <div
                    key={mdl.id}
                    className="flex items-center justify-between py-1.5 border-b border-slate-700/20 last:border-0"
                  >
                    <span className="text-xs text-slate-400 truncate flex-1">
                      {mdl?.name}
                    </span>
                    {renderScoreSummary(score)}
                  </div>
                )
              })}
            </div>
          </Card>
        )}

        {/* Empty state if nothing selected */}
        {!model && !benchmark && (
          <div className="text-center py-8 text-slate-600 text-xs">
            {t('details.empty')}
          </div>
        )}
      </div>
    </aside>
  )
}
