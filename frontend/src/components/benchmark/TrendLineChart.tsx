import React, { useMemo, useRef, useState } from 'react'
import { ModelLogo } from '@/components/ui/ModelLogo'
import type { AIModel, Benchmark, BenchmarkResult, Task } from '@/types'
import { isAutoGeneratedParentTask } from '@/utils/benchmarkTasks'
import { normalizeScore } from '@/utils/scoring'
import { useTranslation } from '@/i18n'

const COLORS = ['#6366f1', '#10b981', '#f59e0b', '#f43f5e', '#06b6d4', '#a855f7', '#ec4899', '#14b8a6']

interface TrendPoint {
  date: string
  timestamp: number
  sequence: number
  percent: number
  label: string
  raw: string
}

interface TrendLineChartProps {
  models: AIModel[]
  benchmarks: Benchmark[]
  results: BenchmarkResult[]
  modelIds?: number[]
  benchmarkIds?: number[]
  title?: string
}

interface TrendTooltip {
  x: number
  y: number
  color: string
  modelId: number
  modelName: string
  benchmarkName: string
  date: string
  percent: number
  raw: string
}

const parseRunAt = (value?: string | null) => {
  if (!value) return 0
  const match = String(value).match(/^(\d{4})-(\d{2})-(\d{2}) (\d{2}):(\d{2})(?::(\d{2}))?$/)
  if (match) {
    const [, year, month, day, hour, minute, second = '0'] = match
    return Date.UTC(Number(year), Number(month) - 1, Number(day), Number(hour), Number(minute), Number(second))
  }
  const parsed = new Date(value).getTime()
  return Number.isFinite(parsed) ? parsed : 0
}

const formatDate = (value: string, locale = 'pl-PL') => {
  const ts = parseRunAt(value)
  if (!ts) return value || '—'
  return new Date(ts).toLocaleString(locale, { day: '2-digit', month: '2-digit', hour: '2-digit', minute: '2-digit' })
}

const getVisibleTasks = (benchmark: Benchmark) => (benchmark.tasks || []).filter((task) => !((benchmark.tasks?.length || 0) > 1 && isAutoGeneratedParentTask(task, benchmark)))

const scoreTaskResult = (result: BenchmarkResult, benchmark: Benchmark, task?: Task) => normalizeScore(
  result.score,
  task?.score_type || benchmark.score_type,
  task?.expected_answer ?? benchmark.expected_answer,
  result.attempt_number,
  task?.attempts || benchmark.attempts
)

const buildRunScore = (
  aggregate: BenchmarkResult,
  previousAggregate: BenchmarkResult | undefined,
  allMatching: BenchmarkResult[],
  benchmark: Benchmark
) => {
  const tasks = getVisibleTasks(benchmark)
  const taskIds = new Set(tasks.map((task) => task.id).filter((id): id is number => typeof id === 'number'))

  if (taskIds.size === 0) {
    const norm = normalizeScore(aggregate.score, benchmark.score_type, benchmark.expected_answer, aggregate.attempt_number, benchmark.attempts)
    return { percent: Math.round(norm * 100), raw: `${Math.round(norm * 100)}%` }
  }

  const taskResults = Array.from(taskIds).map((taskId) => {
    const latest = allMatching
      .filter((result) => {
        if (result.task_id !== taskId) return false
        if (result.id > aggregate.id) return false
        if (previousAggregate && result.id <= previousAggregate.id) return false
        return true
      })
      .sort((a, b) => b.id - a.id)[0]
    const task = tasks.find((item) => item.id === taskId)
    const norm = latest ? scoreTaskResult(latest, benchmark, task) : 0
    return { latest, norm, passed: norm > 0 }
  })

  const passed = taskResults.filter((item) => item.passed).length
  const norm = taskResults.reduce((sum, item) => sum + item.norm, 0) / taskIds.size
  return { percent: Math.round(norm * 100), raw: `${passed}/${taskIds.size}` }
}

export const TrendLineChart: React.FC<TrendLineChartProps> = ({ models, benchmarks, results, modelIds, benchmarkIds, title }) => {
  const { locale, t } = useTranslation()
  const [tooltip, setTooltip] = useState<TrendTooltip | null>(null)
  const hideTooltipTimer = useRef<number | null>(null)

  const clearTooltipTimer = () => {
    if (hideTooltipTimer.current !== null) {
      window.clearTimeout(hideTooltipTimer.current)
      hideTooltipTimer.current = null
    }
  }

  const scheduleHideTooltip = () => {
    clearTooltipTimer()
    hideTooltipTimer.current = window.setTimeout(() => setTooltip(null), 140)
  }

  const showTooltip = (event: React.MouseEvent<SVGCircleElement>, data: Omit<TrendTooltip, 'x' | 'y'>) => {
    clearTooltipTimer()
    const rect = event.currentTarget.ownerSVGElement?.getBoundingClientRect()
    const x = rect ? event.clientX - rect.left : 0
    const y = rect ? event.clientY - rect.top : 0
    setTooltip({ ...data, x, y })
  }

  const chartData = useMemo(() => {
    const modelById = new Map(models.map((model) => [model.id, model]))
    const benchmarkById = new Map(benchmarks.map((benchmark) => [benchmark.id, benchmark]))
    const selectedModelIds = modelIds?.length ? new Set(modelIds) : null
    const selectedBenchmarkIds = benchmarkIds?.length ? new Set(benchmarkIds) : null
    const pointsByModel = new Map<number, TrendPoint[]>()

    for (const model of models) {
      if (selectedModelIds && !selectedModelIds.has(model.id)) continue

      for (const benchmark of benchmarks) {
        if (selectedBenchmarkIds && !selectedBenchmarkIds.has(benchmark.id)) continue
        const matching = results
          .filter((result) => result.model_id === model.id && result.benchmark_id === benchmark.id)
          .sort((a, b) => a.id - b.id)
        if (matching.length === 0) continue

        const aggregates = matching.filter((result) => result.task_id == null).sort((a, b) => a.id - b.id)
        if (aggregates.length > 0) {
          aggregates.forEach((aggregate, index) => {
            const previousAggregate = aggregates[index - 1]
            const runScore = buildRunScore(aggregate, previousAggregate, matching, benchmark)
            const entry = pointsByModel.get(model.id) || []
            entry.push({
              date: aggregate.run_at,
              timestamp: parseRunAt(aggregate.run_at),
              sequence: aggregate.id,
              percent: runScore.percent,
              raw: runScore.raw,
              label: benchmarkById.get(benchmark.id)?.name || `Benchmark #${benchmark.id}`,
            })
            pointsByModel.set(model.id, entry)
          })
          continue
        }

        // Fallback for task-only/manual records without aggregate rows.
        const latestTimestamp = Math.max(...matching.map((result) => parseRunAt(result.run_at)))
        const latestId = Math.max(...matching.map((result) => result.id))
        const tasks = getVisibleTasks(benchmark)
        const taskIds = new Set(tasks.map((task) => task.id).filter((id): id is number => typeof id === 'number'))
        const norm = taskIds.size > 0
          ? Array.from(taskIds).reduce((sum, taskId) => {
            const latest = matching.filter((result) => result.task_id === taskId).sort((a, b) => b.id - a.id)[0]
            const task = tasks.find((item) => item.id === taskId)
            return sum + (latest ? scoreTaskResult(latest, benchmark, task) : 0)
          }, 0) / taskIds.size
          : normalizeScore(matching[matching.length - 1].score, benchmark.score_type, benchmark.expected_answer, matching[matching.length - 1].attempt_number, benchmark.attempts)
        const percent = Math.round(norm * 100)
        const entry = pointsByModel.get(model.id) || []
        entry.push({
          date: matching[matching.length - 1].run_at,
          timestamp: latestTimestamp,
          sequence: latestId,
          percent,
          raw: `${percent}%`,
          label: benchmarkById.get(benchmark.id)?.name || `Benchmark #${benchmark.id}`,
        })
        pointsByModel.set(model.id, entry)
      }
    }

    return Array.from(pointsByModel.entries())
      .map(([modelId, points]) => ({
        modelId,
        modelName: modelById.get(modelId)?.name || `Model #${modelId}`,
        points: points.sort((a, b) => a.timestamp - b.timestamp || a.sequence - b.sequence),
      }))
      .filter((entry) => entry.points.length > 0)
  }, [benchmarks, benchmarkIds, modelIds, models, results])

  if (chartData.length === 0) {
    return (
      <div className="flex h-48 items-center justify-center rounded-xl border border-dashed border-slate-700/60 text-sm text-slate-500">
        {t('trend.empty')}
      </div>
    )
  }

  const allPoints = chartData.flatMap((entry) => entry.points)
  const hasRealTimeRange = new Set(allPoints.map((point) => point.timestamp)).size > 1
  const minTime = Math.min(...allPoints.map((point) => point.timestamp))
  const maxTime = Math.max(...allPoints.map((point) => point.timestamp))
  const timeRange = maxTime - minTime || 1
  const maxSequence = Math.max(1, ...chartData.map((entry) => entry.points.length - 1))

  const padding = { left: 50, right: 30, top: 20, bottom: 60 }
  const chartWidth = Math.max(760, allPoints.length * 70)
  const chartHeight = 340
  const plotWidth = chartWidth - padding.left - padding.right
  const plotHeight = chartHeight - padding.top - padding.bottom
  const yTicks = [0, 25, 50, 75, 100]
  const xTicks = 5

  const getX = (point: TrendPoint, index: number) => {
    if (!hasRealTimeRange) return padding.left + (index / Math.max(maxSequence, 1)) * plotWidth
    return padding.left + ((point.timestamp - minTime) / timeRange) * plotWidth
  }
  const getY = (percent: number) => padding.top + plotHeight - (percent / 100) * plotHeight
  const getVisualPoint = (point: TrendPoint, pointIndex: number, modelIndex: number) => {
    const spread = (modelIndex - (chartData.length - 1) / 2) * 7
    const x = getX(point, pointIndex) + spread * 0.35
    const y = Math.max(padding.top + 4, Math.min(padding.top + plotHeight - 4, getY(point.percent) + spread))
    return { x, y }
  }

  return (
    <div className="space-y-3">
      {title && <p className="text-sm font-semibold text-slate-300">{title}</p>}

      <div className="flex flex-wrap gap-3">
        {chartData.map((entry, index) => (
          <div key={entry.modelId} className="flex items-center gap-2 rounded-xl border border-slate-700/40 bg-slate-950/30 px-3 py-2 text-xs text-slate-400">
            <span className="h-2.5 w-2.5 rounded-full" style={{ backgroundColor: COLORS[index % COLORS.length] }} />
            <ModelLogo model={models.find((model) => model.id === entry.modelId)} size="sm" />
            {entry.modelName}
          </div>
        ))}
      </div>

      <div className="overflow-x-auto rounded-xl border border-slate-700/40 bg-[#1c1f2e] p-4">
        <div className="relative inline-block" onMouseEnter={clearTooltipTimer} onMouseLeave={scheduleHideTooltip}>
        <svg width={chartWidth} height={chartHeight}>
          {yTicks.map((tick) => {
            const y = getY(tick)
            return (
              <g key={tick}>
                <line x1={padding.left} y1={y} x2={chartWidth - padding.right} y2={y} stroke={tick === 50 ? 'rgba(148,163,184,0.22)' : 'rgba(148,163,184,0.09)'} strokeDasharray={tick === 50 ? '' : '4 4'} />
                <text x={padding.left - 8} y={y + 4} fill="#64748b" textAnchor="end" fontSize="11">{tick}%</text>
              </g>
            )
          })}

          {Array.from({ length: xTicks }, (_, index) => {
            const x = padding.left + (index / (xTicks - 1)) * plotWidth
            const ts = minTime + (timeRange / (xTicks - 1)) * index
            return (
              <text key={index} x={x} y={chartHeight - 12} fill="#64748b" textAnchor="middle" fontSize="10">
                {hasRealTimeRange ? new Date(ts).toLocaleDateString(locale, { day: '2-digit', month: '2-digit' }) : `Run ${index + 1}`}
              </text>
            )
          })}

          {chartData.map((entry, modelIndex) => {
            const color = COLORS[modelIndex % COLORS.length]
            const pathD = entry.points.map((point, pointIndex) => {
              const { x, y } = getVisualPoint(point, pointIndex, modelIndex)
              return `${pointIndex === 0 ? 'M' : 'L'} ${x} ${y}`
            }).join(' ')

            return (
              <g key={entry.modelId}>
                {entry.points.length > 1 && <path d={pathD} fill="none" stroke={color} strokeWidth={2.5} strokeLinecap="round" strokeLinejoin="round" opacity={0.9} />}
                {entry.points.map((point, pointIndex) => {
                  const { x, y } = getVisualPoint(point, pointIndex, modelIndex)
                  return (
                    <g key={`${point.sequence}-${pointIndex}`}>
                      <circle cx={x} cy={y} r={4.5} fill={color} stroke="#1c1f2e" strokeWidth={1.5} />
                      <circle
                        cx={x}
                        cy={y}
                        r={16}
                        fill="transparent"
                        className="cursor-pointer"
                        onMouseEnter={(event) => showTooltip(event, { color, modelId: entry.modelId, modelName: entry.modelName, benchmarkName: point.label, date: formatDate(point.date, locale), percent: point.percent, raw: point.raw })}
                        onMouseMove={(event) => showTooltip(event, { color, modelId: entry.modelId, modelName: entry.modelName, benchmarkName: point.label, date: formatDate(point.date, locale), percent: point.percent, raw: point.raw })}
                        onMouseLeave={scheduleHideTooltip}
                      />
                    </g>
                  )
                })}
              </g>
            )
          })}
        </svg>
        {tooltip && (
          <div
            className="pointer-events-none absolute z-20 min-w-64 rounded-xl border border-slate-600/60 bg-[#0b1020]/95 p-3 text-xs shadow-2xl shadow-black/40 backdrop-blur transition-opacity duration-100"
            style={{
              left: Math.max(8, Math.min(tooltip.x + 16, chartWidth - 285)),
              top: Math.max(8, tooltip.y > chartHeight - 145 ? tooltip.y - 130 : tooltip.y + 16),
            }}
          >
            <div className="mb-2 flex items-center gap-2">
              <span className="h-2.5 w-2.5 rounded-full" style={{ backgroundColor: tooltip.color }} />
              <ModelLogo model={models.find((model) => model.id === tooltip.modelId)} size="sm" />
              <span className="font-semibold text-slate-100">{tooltip.modelName}</span>
            </div>
            <p className="text-slate-300">{tooltip.benchmarkName}</p>
            <div className="mt-2 flex items-center justify-between gap-3">
              <span className="text-slate-500">{tooltip.date}</span>
              <span className="font-bold text-slate-100">{tooltip.percent}% <span className="text-slate-500">({tooltip.raw})</span></span>
            </div>
          </div>
        )}
        </div>
      </div>

      <details className="rounded-xl border border-slate-700/40 bg-slate-950/30">
        <summary className="cursor-pointer px-4 py-2 text-sm text-slate-400">{t('trend.points')}</summary>
        <div className="overflow-x-auto p-4">
          <table className="w-full min-w-[720px] text-sm">
            <thead>
              <tr className="border-b border-slate-700/50 text-left text-xs text-slate-500">
                <th className="py-1.5">Model</th>
                <th>{t('common.start')}</th>
                <th>Benchmark</th>
                <th>{t('common.score')}</th>
                <th>Raw</th>
              </tr>
            </thead>
            <tbody>
              {chartData.flatMap((entry) => entry.points.map((point) => ({ entry, point }))).sort((a, b) => b.point.timestamp - a.point.timestamp || b.point.sequence - a.point.sequence).map(({ entry, point }) => (
                <tr key={`${entry.modelId}-${point.sequence}-${point.label}`} className="border-b border-slate-800/60 last:border-0">
                  <td className="py-1.5 text-slate-300">{entry.modelName}</td>
                  <td className="text-slate-400">{formatDate(point.date, locale)}</td>
                  <td className="text-slate-400">{point.label}</td>
                  <td className="font-semibold text-slate-200">{point.percent}%</td>
                  <td className="text-slate-500">{point.raw}</td>
                </tr>
              ))}
            </tbody>
          </table>
        </div>
      </details>
    </div>
  )
}
